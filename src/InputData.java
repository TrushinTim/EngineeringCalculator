
public class InputData {
	private double x;
	private double y;
	private String function;
	
	public InputData(double x, double y, String function) {
		this.x = x;
		this.y = y; 
		this.function = function;
	}
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public String getfunction() {
		return function;
	}

}
